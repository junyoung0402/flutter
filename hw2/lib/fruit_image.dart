class fruit_image {
  String return_image_path(String fruit) {
    if (fruit == 'Orange') {
      return 'assets/image/Orange.jpg';
    } else if (fruit == 'Pineapple') {
      return 'assets/image/Pineapple.jpg';
    } else if (fruit == 'Grapes') {
      return 'assets/image/Grapes.jpg';
    } else if (fruit == 'Watermelon') {
      return 'assets/image/Watermelon.jpg';
    } else if (fruit == 'Strawberry') {
      return 'assets/image/Strawberry.jpg';
    } else if (fruit == 'Peach') {
      return 'assets/image/Peach.jpg';
    } else if (fruit == 'Mango') {
      return 'assets/image/Mango.jpg';
    } else if (fruit == 'Blueberry') {
      return 'assets/image/Blueberry.jpg';
    } else if (fruit == 'Apple') {
      return 'assets/image/Apple.jpg';
    } else if (fruit == 'Banana') {
      return 'assets/image/Banana.jpg';
    } else {
      return 'assets/image/Error.png';
    }
  }
}
